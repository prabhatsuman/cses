void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    map<char, int> mp;
    for (auto x : s)
    {
        mp[x]++;
    }
    int m_count = 0;
    for (auto x : mp)
    {
        m_count = max(m_count, x.second);
    }
    cout << (((n % 2) == 0) ? max(0, m_count - (n - m_count)) : max(1, m_count - (n - m_count))) << nline;
}

