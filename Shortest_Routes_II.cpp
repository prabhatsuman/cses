void solve()
{
    int n,m,q;
    cin>>n>>m>>q;
    vector<vector<ll>> dp(n+1,vector<ll>(n+1,1e18));
    //floyd warshall algorithm
    //O(n^3)
    for(int i=0;i<m;i++)   
    {
        int u,v,w;
        cin>>u>>v>>w;
        dp[u][v]=min(dp[u][v],(ll)w);
        dp[v][u]=min(dp[u][v],(ll)w);
    }
    for(int i=1;i<=n;i++)
    {
        dp[i][i]=0;
    }
    for(int k=1;k<=n;k++)
    {
        for(int i=1;i<=n;i++)
        {
            for(int j=1;j<=n;j++)
            {
                dp[i][j]=min(dp[i][j],dp[i][k]+dp[k][j]);
            }
        }
    }
    while(q--)
    {
        int a,b;
        cin>>a>>b;
        if(dp[a][b]!=1e18)
        {
            cout<<dp[a][b]<<nline;
        }
        else
        {
            cout<<-1<<nline;
        }
    }
}

