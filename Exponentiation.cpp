void solve()
{
    ll a, b;
    cin >> a >> b;
    const int mod = 1e9 + 7;
    function<ll(int, int)> power = [&](int a, int b) -> int
    {
        if (b == 0)
        {
            return 1;
        }
        ll x = power(a, b / 2);
        if (b & 1)
        {
            return ((x % mod * x % mod) % mod * a % mod) % mod;
        }
        else
        {
            return (x % mod * x % mod) % mod;
        }
    };
    cout << power(a, b) << nline;
}

